#include <stdio.h>

int main() {
    int arr[3][3] = {1, 2, 3, 4, 5, 6, 7, 8, 9};
    int* parr[2] = {arr[1], arr[2]};
    printf("%d", parr[1][1] + *parr[1]+2 + **parr); // *parr = parr[0] & **parr = parr[0][0] -> 8 + 9 + 4 = 21
}


// arr
//         arr[0][0]   arr[0][1]   arr[0][2]
// arr[0]  1           2           3
// arr[1]  4           5           6
// arr[2]  7           8           9

// parr
//         parr[0][0]  parr[0][1]  parr[0][2]
// parr[0] 4           5           6
// parr[1] 7           8           9



// ## **parr가 4인 이유
// 사용자님의 질문 **parr = parr[0][0]는 이 문제의 핵심을 꿰뚫는 훌륭한 질문입니다. 결론부터 말하면, 이 코드에서는 두 표현이 결과적으로 같은 값(4)을 가리키는 것이 맞습니다. 
// 그 이유는 다음과 같습니다.

//     parr의 정체: int* parr[2]는 int형 포인터를 2개 담을 수 있는 배열입니다.

//     배열의 포인터 변환: C언어에서 배열의 이름(parr)을 연산에 사용하면, 그 이름은 배열의 첫 번째 요소를 가리키는 포인터로 자동 변환됩니다.

//         parr 배열의 첫 번째 요소는 parr[0]입니다.

//         따라서 parr는 parr[0]의 주소를 가리키는 **이중 포인터(int**)**처럼 동작합니다.

//     첫 번째 역참조 *parr: parr를 한 번 역참조하면 첫 번째 요소인 parr[0]의 값에 접근합니다.

//         *parr는 parr[0]과 같습니다.

//         parr[0]에는 2D 배열 arr의 두 번째 행의 시작 주소인 arr[1]이 저장되어 있습니다.

//     두 번째 역참조 **parr: *parr를 한 번 더 역참조합니다.

//         **parr는 *(parr[0])과 같습니다.

//         parr[0]은 arr[1]의 시작 주소(즉, arr[1][0]의 주소)를 가지고 있습니다.

//         따라서 *(parr[0])은 arr[1][0]의 실제 값인 4가 됩니다.

//         💡 비교: parr[0][0]는 *(parr[0] + 0)와 같으므로, *(parr[0])과 동일한 표현입니다. 따라서 **parr와 parr[0][0]은 같은 값을 나타냅니다.

// ## 포인터와 이중 포인터 개념

// ### 포인터 (Pointer, int*) 🔑
// 포인터는 변수의 메모리 주소를 저장하는 특별한 변수입니다.

//     & (주소 연산자): 변수 앞에 붙여 그 변수의 메모리 주소를 가져옵니다.

//     * (역참조 연산자): 포인터 변수 앞에 붙여 그 주소에 저장된 실제 값을 가져옵니다.

// 비유:

//     int a = 10;: 'a'라는 집에 10이 살고 있습니다.

//     int* p = &a;: p라는 쪽지에 'a' 집의 주소를 적어둡니다.

// ### 이중 포인터 (Double Pointer, int**) 📜
// 이중 포인터는 다른 포인터 변수의 메모리 주소를 저장하는 변수입니다.

// 비유:

//     int a = 10;: 'a'라는 집에 10이 살고 있습니다.

//     int* p = &a;: p라는 쪽지에 'a' 집의 주소를 적어둡니다.

//     int** pp = &p;: pp라는 다른 쪽지에 p 쪽지를 보관해 둔 서랍의 주소를 적어둡니다.

// 이때 pp를 통해 a의 값에 접근하려면:

//     *pp: pp에 적힌 주소로 찾아가 p 쪽지를 꺼냅니다. (*pp는 p와 같습니다.)

//     **pp: 꺼낸 p 쪽지에 적힌 주소로 찾아가 a 집의 값인 10을 가져옵니다. (**pp는 *p와 같고, 최종적으로 a의 값입니다.)