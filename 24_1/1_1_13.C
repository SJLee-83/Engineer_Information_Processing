#include <stdio.h>
#include <ctype.h>

int main() {
    char *p = "It is 8";
    char result[100];
    int i;
    for(i = 0; p[i] != '\0'; i++) {
        if(isupper(p[i]))
            result[i] = (p[i] - 'A' + 5) % 25 + 'A';
        else if(islower(p[i]))
            result[i] = (p[i] - 'a' + 10) % 26 + 'a';
        else if(isdigit(p[i]))
            result[i] = (p[i] - '0' + 3) % 10 + 'a';
        else if(!(isupper(p[i]) || islower(p[i]) || isdigit(p[i])))
            result[i] = p[i];
    }
    result[i] = '\0';
    printf("변환된 문자열 : %s\n", result);
    return 0; 
}

// ## 코드 실행 과정
// for 루프가 "It is 8"의 각 문자를 따라가며 아래와 같이 변환합니다.

//     'I' (대문자): ('I' - 'A' + 5) % 25 + 'A'

//         I는 A로부터 8번째 문자이므로 (8 + 5) % 25 + 'A'

//         13 % 25 + 'A' → 13 + 'A' → A로부터 13번째 문자인 **'N'**이 됩니다.

//     't' (소문자): ('t' - 'a' + 10) % 26 + 'a'

//         t는 a로부터 19번째 문자이므로 (19 + 10) % 26 + 'a'

//         29 % 26 + 'a' → 3 + 'a' → a로부터 3번째 문자인 **'d'**가 됩니다.

//     ' ' (공백): 대/소문자나 숫자가 아니므로 그대로 **' '**가 됩니다.

//     'i' (소문자): ('i' - 'a' + 10) % 26 + 'a' → (8 + 10) % 26 + 'a' → 18 + 'a' → **'s'**가 됩니다.

//     's' (소문자): ('s' - 'a' + 10) % 26 + 'a' → (18 + 10) % 26 + 'a' → 2 + 'a' → **'c'**가 됩니다.

//     ' ' (공백): 그대로 **' '**가 됩니다.

//     '8' (숫자): ('8' - '0' + 3) % 10 + 'a'

//         문자 '8'은 숫자 8을 의미하므로 (8 + 3) % 10 + 'a'

//         11 % 10 + 'a' → 1 + 'a' → a로부터 1번째 문자인 **'b'**가 됩니다.

// 모든 변환이 끝나면 result 배열에는 "Nd sc b"가 저장되고, 이것이 최종적으로 출력됩니다.

// ## 1. <ctype.h>와 문자 함수들
// #include <ctype.h>는 C언어의 표준 라이브러리 헤더 파일로, **문자의 종류를 판별(classify)하거나 변환(transform)**하는 유용한 함수들을 모아놓은 것입니다. ctype은 character type의 약자입니다.

//     isupper(char c): 전달된 문자 c가 대문자('A'-'Z')이면 참(0이 아닌 값)을, 아니면 거짓(0)을 반환합니다.

//     islower(char c): 전달된 문자 c가 소문자('a'-'z')이면 참을, 아니면 거짓을 반환합니다.

//     isdigit(char c): 전달된 문자 c가 숫자('0'-'9')이면 참을, 아니면 거짓을 반환합니다.

// %s (String): 문자열을 출력합니다. null 문자(\0)를 만날 때까지 연속된 문자들을 출력합니다.

// %d (Decimal): 부호 있는 10진수 정수(int)를 출력합니다.

// %c (Character): 단일 문자 하나를 출력합니다.

// %f (Floating-point): 실수(float, double)를 출력합니다.

// \n (New Line): 서식 지정자는 아니지만 함께 자주 쓰이는 **이스케이프 시퀀스(Escape Sequence)**로, 줄바꿈을 의미합니다.

