#include <stdio.h>
void func (int** arr, int size){
    for(int i = 0; i < size; i++){
        *(*arr + i) = (*(*arr + i) + i) % size;
    }
}

int main(){
    int arr[] = {3, 1, 4, 1, 5};
    int* p = arr;
    int** pp = &p; // 
    int num = 6;
    func(pp, 5);
    num = arr[2];
    printf("%d", num);
    return 0;
}

// ## 핵심 개념 설명
// ### 포인터(int*)와 이중 포인터(int**) 🔑
    // 포인터(*): 메모리의 주소를 저장하는 변수입니다. 코드에서 p는 정수 배열 arr의 시작 주소를 저장합니다.

    // 이중 포인터(**): 다른 포인터의 주소를 저장하는 변수입니다. 코드에서 pp는 포인터 변수 p 자체의 주소를 저장합니다. 
    // pp를 통하면 p를 제어할 수 있고, p를 통하면 arr를 제어할 수 있는 구조입니다.

// ### 포인터를 이용한 참조에 의한 호출 (Call by Reference) 🔗
// C언어에서 함수는 기본적으로 값을 복사해서 전달합니다(Call by Value). 하지만 포인터를 사용하면 변수의 주소를 전달하여 함수가 원본 데이터를 직접 수정하게 할 수 있습니다. 
// 이 코드에서는 func 함수가 main 함수의 arr 배열을 직접 수정하기 위해 이중 포인터 pp를 사용했습니다.

// ### 포인터 연산과 역참조(*)
    // 역참조(*): 포인터가 가리키는 주소에 저장된 실제 값을 가져오는 연산입니다.

    // *(*arr + i) 분석:

        // *arr: func의 arr는 pp의 주소 값을 가지고 있으므로, 첫 번째 *는 p를 가져옵니다.

        // p + i: p는 arr 배열의 시작 주소이므로, i만큼 떨어진 요소(즉, arr[i])의 주소를 계산합니다.

        // *(p + i): arr[i]의 주소에 있는 실제 값을 최종적으로 가져옵니다.